package sch.frog.kit.core;

public enum ValueType {

    /**
     * 布尔值
     */
    BOOL,
    /**
     * 数字
     */
    NUMBER,
    /**
     * 字符串
     */
    STRING,
    /**
     * 对象, 类json格式
     */
    OBJECT,
    /**
     * 数组, 类json格式
     */
    ARRAY,
    /**
     * 函数
     */
    FUNCTION,
    /**
     * 空值
     */
    VOID;

}
